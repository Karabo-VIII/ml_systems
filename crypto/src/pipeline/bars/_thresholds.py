"""Shared threshold loader for alt-bar builders.

Single source of truth for per-asset thresholds across DIB / adaptive_vol /
tick_runs / vol_runs builders. Resolves in this order:

  1. Explicit hardcoded table in the builder (preserves u10 hand-tuning)
  2. Per-asset override in config/alt_bar_thresholds.yaml
  3. Tier-based default in config/alt_bar_thresholds.yaml
  4. Hardcoded safe fallback

Generated by scripts/oracle/derive_alt_bar_thresholds.py from
config/universes/u50.yaml + u100.yaml median_dollar_vol_30d values.

NOTE on vol_runs: the per-asset and tier defaults for vol_runs are derived
from a coarse avg-price proxy (per liquidity_tier). For production use,
recompute these from per-asset aggTrades volume scans. For first-pass
exhaust, tick_runs is the preferred consensus-flow signal.
"""
